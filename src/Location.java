
public class Location {
    private int row;
    private int col;


    public Location(int row, int col) {
        this.row = row;
        this.col = col;
    }


    /**
     * @return the row
     */
    public int getRow() {
        return row;
    }


    /**
     * @return the col
     */
    public int getCol() {
        return col;
    }

}
